package net.highwayfrogs.editor.games.sony.oldfrogger.map;

/**
 * Represents different versions of the old frogger map file format.
 * Created by Kneesnap on 12/12/2023.
 */
public enum OldFroggerMapVersion {
    EARLY, // This format identifies map files created loosely between January 1997 and February 1997 seen as old files included in the Milestone 3 PC Build.
    MILESTONE3 // This is the format of the levels included from March 1997 playable in the Milestone 3 builds.
}